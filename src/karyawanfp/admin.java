    private void datatabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_datatabelMouseClicked
        // TODO add your handling code here:
         try {
            int row = datatabel.getSelectedRow();
            String tabel_klik = (datatabel.getModel().getValueAt(row, 0).toString());
            String sql = "select * from admin where id='" + tabel_klik + "'";
            pst = con.prepareStatement(sql);
            rs = pst.executeQuery();
            if (rs.next()) {
                String data1 = rs.getString(("id"));
                id_field.setText(data1);
                String data2 = rs.getString(("nama"));
                namafield.setText(data2);
                String data3 = rs.getString(("jabatan"));
                jabatanfield.setText(data3);
                String data4 = rs.getString(("status"));
                status.setText(data4);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_datatabelMouseClicked

